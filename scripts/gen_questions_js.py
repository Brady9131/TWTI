DELETE_0 = {11, 14, 17}  # Q12, Q15, Q18

def fmt_dims(d4):
    return "[" + ", ".join(str(x) for x in d4) + "]"

def emit_question(qobj):
    lines = ['  {']
    lines.append(f'    q: {json.dumps(qobj["q"], ensure_ascii=False)},')
    lines.append('    options: [')
    for o in qobj['options']:
        d4 = conv(o['dims'])
        lines.append('      {')
        lines.append(f'        text: {json.dumps(o["text"], ensure_ascii=False)},')
        lines.append(f'        dims: {fmt_dims(d4)},')
        lines.append('      },')
    lines.append('    ],')
    lines.append('  },')
    return '\n'.join(lines)

out = []
for i, q in enumerate(RAW):
    if i in DELETE_0:
        continue
    out.append(emit_question(q))

